    private void getData(int cari) {

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Asset Nomor");
        model.addColumn("Asset Nama");
        model.addColumn("CPU");
        model.addColumn("Ram");
        model.addColumn("HDD");
        model.addColumn("OS");
        model.addColumn("IP Address");
        model.addColumn("Status PC");
        model.addColumn("Computer Name");
        model.addColumn("Dept");
        model.addColumn("Location");

        try {

            if (cari == 0) {
                sql = "SELECT [asset_nomor]\n"
                        + "      ,[asset_name]\n"
                        + "      ,[cpu]\n"
                        + "      ,[ram]\n"
                        + "      ,[hdd]\n"
                        + "      ,[os]\n"
                        + "      ,[ipaddress]\n"
                        + "      ,[statuspc]\n"
                        + "      ,[computer_name]\n"
                        + "      ,[dept]\n"
                        + "      ,[location]\n"
                        + "  FROM [dbo].[tbl_assetlist]";
            } else {
                sql = "SELECT [asset_nomor]\n"
                        + "      ,[asset_name]\n"
                        + "      ,[cpu]\n"
                        + "      ,[ram]\n"
                        + "      ,[hdd]\n"
                        + "      ,[os]\n"
                        + "      ,[ipaddress]\n"
                        + "      ,[statuspc]\n"
                        + "      ,[computer_name]\n"
                        + "      ,[dept]\n"
                        + "      ,[location]\n"
                        + "  FROM [dbo].[tbl_assetlist] where asset_name = '" + edt_cari.getText() + "'";
            }

            connection = C_Connection.getConnection();
            stt = connection.createStatement();
            res = stt.executeQuery(sql);

            while (res.next()) {
                model.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5), res.getString(6), res.getString(7), res.getString(8), res.getString(9), res.getString(10), res.getString(11)});
            }

            res.close();
            stt.close();
            JTAset.setModel(model);

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

    }

    private void getDept() {

        try {
            sql = "SELECT name FROM [dbo].[organization]";
            connection = C_Connection.getConnection();
            stt = connection.createStatement();
            res = stt.executeQuery(sql);

            while (res.next()) {
                JC_Dept.addItem(res.getString(1).toString());
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

    }

