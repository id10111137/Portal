    private int CountData(String Inputan) {
        int Hasil = 0;
        try {
            sql = "SELECT COUNT(asset_nomor) as jml FROM [dbo].[tbl_assetlist] WHERE asset_nomor = '" + Inputan + "'";

            connection = C_Connection.getConnection();
            stt = connection.createStatement();
            res = stt.executeQuery(sql);

            while (res.next()) {
                Hasil = res.getInt(1);
            }

            res.close();
            stt.close();

        } catch (SQLException e) {
            e.printStackTrace();
        }
        return Hasil;
    }

