    public final BufferedImage decodeToImage(String imageString) {

        BufferedImage image = null;
        byte[] imageByte;
        try {
            BASE64Decoder decoder = new BASE64Decoder();
            imageByte = decoder.decodeBuffer(imageString);
            try (ByteArrayInputStream bis = new ByteArrayInputStream(imageByte)) {
                image = ImageIO.read(bis);
                image.getScaledInstance(30, 50, 30);
            }
        } catch (IOException e) {
        }
        return image;
    }

    private void EnableAfterEdeting() {

        if (hellper_Portal.getDept(_Nik) == null) {
            jc_status.setEnabled(false);
            txt_komentar.setEnabled(false);
            jButton1.setEnabled(false);
            JC_Dept.setEnabled(false);
            JC_Users.setEnabled(false);
            txt_catatan.setEnabled(false);
            JbKirim.setEnabled(false);
        } else if (hellper_Portal.getDept(_Nik).equals("IT Department")) {
            jc_status.setEnabled(true);
            txt_komentar.setEnabled(true);
            jButton1.setEnabled(true);
            JC_Dept.setEnabled(true);
            JC_Users.setEnabled(true);
            txt_catatan.setEnabled(true);
            JbKirim.setEnabled(true);
        } else {
            if (hellper_Portal.CountClose(_IdHelpDesk) != 0) {
                jc_status.setEnabled(false);
                txt_komentar.setEnabled(false);
                jButton1.setEnabled(false);
                JC_Dept.setEnabled(false);
                JC_Users.setEnabled(false);
                txt_catatan.setEnabled(false);
                JbKirim.setEnabled(false);
            }
        }

    }

