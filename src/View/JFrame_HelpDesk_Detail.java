    private void getDataKompentar() {

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Status");
        model.addColumn("Pesan");

        try {
            sql = "SELECT \n"
                    + "      [nomor_statuspekerjaan],[status_pekerjaan], [komentar]\n"
                    + "  FROM [dbo].[tbl_status_pekerjaan] WHERE nomor_ticket = '" + _IdHelpDesk + "'";

            connection = C_Connection.getConnection();
            stt = connection.createStatement();
            res = stt.executeQuery(sql);

            while (res.next()) {
                model.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3)});
            }
            res.close();
            stt.close();
            Jt_Status_pekerjaan.setModel(model);
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

    }

