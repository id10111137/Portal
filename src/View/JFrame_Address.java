    private void UpdateAction(int setUpdate) {

        try {
            sql = "SELECT nama_cust, alamat, telp FROM [192.168.12.12].[dbstore_ho].[dbo].[t_takeorder_user] WHERE nama_cust = '" + (tbl_user.getValueAt(setUpdate, 0).toString()) + "'";
            connection = C_Connection.getConnection();
            stt = connection.createStatement();
            res = stt.executeQuery(sql);

            while (res.next()) {
                edt_nama.setText(res.getString(1));
                edt_alamat.setText(res.getString(2));
                edt_tlp.setText(res.getString(3));
                Action = 1;
                btn_action.setText("Update");
            }
            res.close();
            stt.close();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

    private int Action(int baris) {
        int Hasil = 0;
        if (baris == 0) {
            try {
                sql = "INSERT INTO [192.168.12.12].[dbstore_ho].[dbo].[t_takeorder_user] \n"
                        + "(nama_cust, alamat, telp)\n"
                        + "VALUES ('" + edt_nama.getText() + "','" + edt_alamat.getText() + "','" + edt_tlp.getText() + "')";

                connection = C_Connection.getConnection();
                ps = connection.prepareStatement(sql);
                ps.execute();
                JOptionPane.showMessageDialog(null, "Terima Kasih Data Sudah Di Simpan");
                getUser(statusSearching);
            } catch (SQLException e) {
                e.printStackTrace();
            }
        } else {
            try {
                sql = "UPDATE [192.168.12.12].[dbstore_ho].[dbo].[t_takeorder_user] SET nama_cust = '" + edt_nama.getText() + "', alamat = '" + edt_alamat.getText() + "' telp = '" + edt_tlp.getText() + "' where nama = '" + edt_nama.getText() + "'";

                stt = connection.createStatement();
                int executeUpdate = stt.executeUpdate(sql);
                if (executeUpdate >= 1) {
                    JOptionPane.showMessageDialog(null, "Terima Kasih Update Berhasil");
                } else {
                    JOptionPane.showMessageDialog(null, "Mohon Maaf Update Gagal");
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }

        return Hasil;
    }

