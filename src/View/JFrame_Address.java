    private void getUser(int _statusSearching) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("Telp");

        try {

            if (_statusSearching == 0) {
                sql = "SELECT nama_cust, alamat, telp FROM [192.168.12.12].[dbstore_ho].[dbo].[t_takeorder_user]";
            } else {
                sql = "SELECT nama_cust, alamat, telp FROM [192.168.12.12].[dbstore_ho].[dbo].[t_takeorder_user] WHERE nama_cust ='" + edt_cari.getText() + "'";
            }

            connection = C_Connection.getConnection();
            stt = connection.createStatement();
            res = stt.executeQuery(sql);

            while (res.next()) {
                model.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3)});
            }
            res.close();
            stt.close();
            tbl_user.setModel(model);
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

